                                   # [ Simple Calculator using Python ]

print('"Calculator"')
num1=float(input("Enter First Number: "))
mode=input("Chose an Operation(+,-,*,/): ")
num2=float(input("Enter Second Number: "))

if mode == "+":
    sum = num1 + num2
    print(sum)
elif mode == "-":
    subtract = num1 - num2
    print(subtract)
elif mode == "*":
    multiple = num1 * num2
    print(multiple)
elif mode == "/":
    divide = num1 / num2
    print(divid)
else:
    print("Invalid Entery")























